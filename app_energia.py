# --- PROCESAMIENTO DE DATOS ---
@st.cache_data
def procesar_excel(uploaded_file, sheet_name="TABLA HORARIA"):
    """Carga Excel, realiza Unpivot y limpia datos."""
    try:
        df = pd.read_excel(uploaded_file, sheet_name=sheet_name)
        id_vars = df.columns[:6].tolist()
        value_vars = df.columns[6:].tolist()

        df_melted = df.melt(id_vars=id_vars, value_vars=value_vars, var_name='Hora', value_name='Energia')
        df_melted['fecha'] = pd.to_datetime(df_melted['fecha'], errors='coerce')
        df_melted['Energia'] = pd.to_numeric(df_melted['Energia'], errors='coerce')
        df_melted = df_melted.dropna(subset=['fecha', 'Energia'])

        # Ingenier√≠a de variables
        df_melted['Hora_Num'] = df_melted['Hora'].str.replace('H', '').astype(int)
        df_melted['Dia_Nombre'] = df_melted['fecha'].dt.day_name(locale='es')
        df_melted['Mes_Nombre'] = df_melted['fecha'].dt.month_name(locale='es')
        df_melted['Semana'] = df_melted['fecha'].dt.isocalendar().week

        # Asignaci√≥n de Tarifas
        def categorizar_tarifa(h):
            if 13 <= h <= 18: return 'Alta (Punta)'
            elif (7 <= h <= 12) or (19 <= h <= 22): return 'Media (Valle)'
            return 'Baja (Super Valle)'

        df_melted['Tarifa'] = df_melted['Hora_Num'].apply(categorizar_tarifa)

        # Costos base sugeridos
        precios = {'Alta (Punta)': 0.25, 'Media (Valle)': 0.15, 'Baja (Super Valle)': 0.10}
        df_melted['Costo_kWh'] = df_melted['Tarifa'].map(precios)
        df_melted['Costo_Total'] = df_melted['Energia'] * df_melted['Costo_kWh']

        return df_melted
    except Exception as e:
        st.error(f"Error procesando el archivo: {e}")
        return None

