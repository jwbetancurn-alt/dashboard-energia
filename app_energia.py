# --- PROCESAMIENTO DE DATOS ---
@st.cache_data
def procesar_archivo(uploaded_file):
    """Carga Excel o CSV, realiza Unpivot y limpia datos con manejo robusto de NaN."""
    try:
        # Detectar tipo de archivo
        if uploaded_file.name.endswith('.csv'):
            df = pd.read_csv(uploaded_file, sep=None, engine='python')
        else:
            try:
                df = pd.read_excel(uploaded_file, sheet_name="TABLA HORARIA")
            except:
                df = pd.read_excel(uploaded_file)

        # Limpiar nombres de columnas y eliminar columnas completamente vac√≠as
        df.columns = df.columns.str.strip()
        df = df.dropna(how='all', axis=0) # Eliminar filas vac√≠as

        # Normalizar nombres para b√∫squeda
        cols_lower = [c.lower() for c in df.columns]

        if 'fecha' in cols_lower:
            # Identificar columnas de horas (asumiendo formato H1, H2... o similar)
            id_vars = [c for c in df.columns if not (c.startswith('H') and any(char.isdigit() for char in c))]
            value_vars = [c for c in df.columns if (c.startswith('H') and any(char.isdigit() for char in c))]

            if value_vars:
                # Unpivot: de formato ancho a largo
                df = df.melt(id_vars=id_vars, value_vars=value_vars, var_name='Hora_Raw', value_name='Energia')

            # Normalizaci√≥n post-melt
            df.columns = [c.lower() for c in df.columns]
            col_fecha = next(c for c in df.columns if 'fecha' in c)
            col_energia = next(c for c in df.columns if 'energia' in c)

            # Limpieza cr√≠tica de nulos antes de conversiones
            df = df.dropna(subset=[col_fecha, col_energia])

            df['fecha_dt'] = pd.to_datetime(df[col_fecha], errors='coerce')
            df['kWh'] = pd.to_numeric(df[col_energia], errors='coerce')
            df = df.dropna(subset=['fecha_dt', 'kWh'])

            # Manejo de Hora evitando el error de NaN a Integer
            if 'hora_raw' in df.columns:
                # Extraer n√∫meros y manejar posibles nulos resultantes
                extracted_hour = df['hora_raw'].astype(str).str.extract('(\d+)')
                df['Hora_Num'] = pd.to_numeric(extracted_hour[0], errors='coerce').fillna(0).astype(int)
            else:
                df['Hora_Num'] = df['fecha_dt'].dt.hour

            df['Dia_Nombre'] = df['fecha_dt'].dt.day_name(locale='es')
            df['Mes_Nombre'] = df['fecha_dt'].dt.month_name(locale='es')

            # Asignaci√≥n de Tarifas
            def categorizar_tarifa(h):
                if 13 <= h <= 18: return 'Alta (Punta)'
                elif (7 <= h <= 12) or (19 <= h <= 22): return 'Media (Valle)'
                return 'Baja (Super Valle)'

            df['Tarifa'] = df['Hora_Num'].apply(categorizar_tarifa)
            df['Costo_Total'] = df['kWh'] * 0.20

            return df
        else:
            st.error("No se encontr√≥ la columna 'fecha' en el archivo.")
            return None
    except Exception as e:
        st.error(f"Error procesando el archivo: {e}")
        return None

