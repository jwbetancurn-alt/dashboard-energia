# --- PROCESAMIENTO DE DATOS ---
@st.cache_data
def procesar_archivo(uploaded_file):
    """Carga Excel o CSV, realiza Unpivot y limpia datos."""
    try:
        # Detectar tipo de archivo
        if uploaded_file.name.endswith('.csv'):
            df = pd.read_csv(uploaded_file, sep=None, engine='python')
        else:
            # Intenta leer Excel, buscando una pesta√±a espec√≠fica o la primera por defecto
            try:
                df = pd.read_excel(uploaded_file, sheet_name="TABLA HORARIA")
            except:
                df = pd.read_excel(uploaded_file)

        # Limpiar nombres de columnas
        df.columns = df.columns.str.strip()

        # L√≥gica de Unpivot (ajustar seg√∫n estructura detectada)
        if 'fecha' in df.columns.str.lower():
            id_vars = [c for c in df.columns if not c.startswith('H')]
            value_vars = [c for c in df.columns if c.startswith('H')]

            if value_vars:
                df = df.melt(id_vars=id_vars, value_vars=value_vars, var_name='Hora', value_name='Energia')

            # Normalizaci√≥n de nombres de columnas clave
            df.columns = [c.lower() for c in df.columns]
            col_fecha = next(c for c in df.columns if 'fecha' in c)
            col_energia = next(c for c in df.columns if 'energia' in c)

            df['fecha_dt'] = pd.to_datetime(df[col_fecha], errors='coerce')
            df['kWh'] = pd.to_numeric(df[col_energia], errors='coerce')
            df = df.dropna(subset=['fecha_dt', 'kWh'])

            # Ingenier√≠a de variables
            if 'hora' in df.columns:
                df['Hora_Num'] = df['hora'].str.extract('(\d+)').astype(int)
            else:
                df['Hora_Num'] = df['fecha_dt'].dt.hour

            df['Dia_Nombre'] = df['fecha_dt'].dt.day_name(locale='es')
            df['Mes_Nombre'] = df['fecha_dt'].dt.month_name(locale='es')

            # Asignaci√≥n de Tarifas Simplificada
            def categorizar_tarifa(h):
                if 13 <= h <= 18: return 'Alta (Punta)'
                elif (7 <= h <= 12) or (19 <= h <= 22): return 'Media (Valle)'
                return 'Baja (Super Valle)'

            df['Tarifa'] = df['Hora_Num'].apply(categorizar_tarifa)
            df['Costo_Total'] = df['kWh'] * 0.20 # Costo estimado base

            return df
        else:
            st.error("No se encontr√≥ la columna 'fecha' en el archivo.")
            return None
    except Exception as e:
        st.error(f"Error procesando el archivo: {e}")
        return None

